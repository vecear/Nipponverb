// 所有職業故事的匯總
export const JOB_STORIES: Record<string, CharacterStories> = {
  doshin: DOSHIN_STORIES,
  yakusha: YAKUSHA_STORIES,
  ukiyoeshi: UKIYOESHI_STORIES,
  shonin: SHONIN_STORIES,
  gakusha: GAKUSHA_STORIES,
  onmyoji: ONMYOJI_STORIES,
  ryorinin: RYORININ_STORIES,
  hokan: HOKAN_STORIES,
}

/**
 * 取得角色目前等級的故事
 */
export const getCharacterStory = (
  level: number,
  jobId: string | null,
  gender: 'male' | 'female'
): string => {
  // 等級 0-4 或未選擇職業：使用初心者故事
  if (level < 5 || !jobId) {
    const noviceStories = NOVICE_STORIES[gender]
    const story = noviceStories.find(s => level >= s.minLevel && level <= s.maxLevel)
    return story?.story || '你的冒險即將開始...'
  }

  // 取得職業故事
  const jobStories = JOB_STORIES[jobId]
  if (!jobStories) {
    return '你的故事正在書寫中...'
  }

  const stories = jobStories[gender]
  const story = stories.find(s => level >= s.minLevel && level <= s.maxLevel)
  return story?.story || '你的故事正在書寫中...'
}
